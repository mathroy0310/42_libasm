;/*	MANAGE SYSCALLS ERRORS AND RETURNS -1 									   */#
;/* ************************************************************************** */#

section .text
extern ___error

_ft_error:
	push rax
	call ___error
	pop rdx
	mov [rax], rdx
	mov rax, -1
	ret

